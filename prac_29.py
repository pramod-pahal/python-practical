# Create a tuple
my_tuple = (1, 2, 3, 4, 5, 6, 7)

# (a) Indexing - print the 1st item and 3rd item
print("First item:", my_tuple[0])
print("Third item:", my_tuple[2])

# (b) Negative indexing - print the 3rd last element
print("Third last element:", my_tuple[-3])

# (c) Slicing - print elements from 2nd index till the end
print("Elements from 2nd index till the end:", my_tuple[1:])

# (d) Slicing - print elements from 2nd index to 5th index
print("Elements from 2nd index to 5th index:", my_tuple[1:5])
